class logical_op
{
    public static void main(String args[])
    {
        int a = 10;
        int b = 15;
        if( a == b )
        {
            System.out.println("Both A & B Number are same");
        }
        else if( a > b)
        {
            System.out.println("A is Grater than B ");
        }
        else
        {
            System.out.println("B is Grater than A");
        }
    }
}