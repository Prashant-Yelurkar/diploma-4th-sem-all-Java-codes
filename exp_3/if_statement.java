class if_statement
{
    public static void main(String args[])
    {
        int a = -1;
        if( a > 0)
        {
            System.out.println(a + " is Positive Number");
        }
        if( a < 0)
        {
            System.out.println(a + " is Negative Number");
        }
        if( a == 0 )
        {
            System.out.println(a + " it is Zero");
        }
    }
}